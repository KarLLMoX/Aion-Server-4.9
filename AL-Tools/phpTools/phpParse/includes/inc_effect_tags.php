// ----------------------------------------------------------------------------
// Effect-Tag aus Client für EMU zurückgeben
// ----------------------------------------------------------------------------
function getEmuEffectTag($cname)
{
    global $tabefftags;
    
    $key = strtolower($cname);
    
    if (isset($tabefftags[$key]))
        return $tabefftags[$key];
    else
        return $key;
}